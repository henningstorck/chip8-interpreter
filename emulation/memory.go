package emulation

import (
	"fmt"
	"os"
)

var fontSet = []uint8{
	0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
	0x20, 0x60, 0x20, 0x20, 0x70, // 1
	0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
	0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
	0x90, 0x90, 0xF0, 0x10, 0x10, // 4
	0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
	0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
	0xF0, 0x10, 0x20, 0x40, 0x40, // 7
	0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
	0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
	0xF0, 0x90, 0xF0, 0x90, 0x90, // A
	0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
	0xF0, 0x80, 0x80, 0x80, 0xF0, // C
	0xE0, 0x90, 0x90, 0x90, 0xE0, // D
	0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
	0xF0, 0x80, 0xF0, 0x80, 0x80, // F
}

type Memory struct {
	Data [4096]uint8
	Init [4096]uint8
}

func (memory *Memory) ReadWord(address uint16) uint16 {
	return uint16(memory.Data[address])<<8 | uint16(memory.Data[address+1])
}

func (memory *Memory) LoadProgram(fileName string, offset int) error {
	file, fileErr := os.OpenFile(fileName, os.O_RDONLY, 0777)

	if fileErr != nil {
		return fileErr
	}

	defer file.Close()
	fStat, fStatErr := file.Stat()

	if fStatErr != nil {
		return fStatErr
	}

	if int64(len(memory.Init)-offset) < fStat.Size() {
		return fmt.Errorf("program is bigger than memory")
	}

	buffer := make([]byte, fStat.Size())

	if _, readErr := file.Read(buffer); readErr != nil {
		return readErr
	}

	copy(memory.Init[offset:], buffer)
	memory.Reset()
	return nil
}

func (memory *Memory) Reset() {
	memory.Data = memory.Init
	copy(memory.Data[0:len(fontSet)], fontSet)
}
